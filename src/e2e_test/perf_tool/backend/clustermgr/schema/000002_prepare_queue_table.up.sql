CREATE TABLE cluster_prepare_queue (
  -- internal id used to order the queue.
  qid int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  -- experiment_uuid of the experiment requesting a cluster.
  experiment_uuid UUID,
  -- experiment_retry_idx together with experiment_uuid, uniquely identifies a particular run of an experiment.
  experiment_retry_idx int,
  -- serialized_spec is the experimentpb.ClusterSpec serialized to bytes
  serialized_spec bytea
);
